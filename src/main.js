import { Circle, Rectangle } from "shapes"

let c = new Circle(30, 20, 10)
print("c", c)
print("c.x", c.x)
print("c.y", c.y)
print("c.r", c.r)
print("c.area", c.area)

let r = new Rectangle(10, 20, 30, 40)
print("r", r)
print("r.x", r.x)
print("r.y", r.y)
print("r.w", r.w)
print("r.h", r.h)
print("r.area", r.area)
